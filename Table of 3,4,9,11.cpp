#include<stdio.h>
int main(){
	int i,j,k,l=1;
	while(i<=10){
		printf("3 x %d = %d\n",i,3*i);
		i++;
	}
	printf("\n");
	
	while(j<=10){
		printf("4 x %d = %d\n",j,4*j);
		j++;
	}
	printf("\n");
	while(k<=10){
		printf("9 x %d = %d\n",k,9*k);
		k++;
	}
	printf("\n");
	while(l<=10){
		printf("11 x %d = %d\n",l,11*l);
		l++;
	}
		
}
